#include "../includes/ft_printf.h"

void	field(char **str, t_form *form)
{
	if (**str == 's' || **str == 'S' || **str == 'p' || **str == 'd' ||
			**str == 'D' || **str == 'i' || **str == 'o' || **str == 'O' ||
			**str == 'u' || **str == 'U' || **str == 'x' || **str == 'X' ||
			**str == 'c' || **str == 'C' || **str == 'b' || **str == 'B' ||
			**str == '%')
		form->field = *(*str)++;
	else
		form->field = 0;
}
