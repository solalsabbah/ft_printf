int		ft_convert_oct(int nn)
{
	int o;
	int nb;
	int j;

	o = n;

	nb = 0;
	j = 0;
	while (j > 0)
	{
		nb = nb * 10 + (j % 8);
		j = j / 8;
	}
	while (nb > 0)
	{
		o = o * 10 + (nb % 10);
		nb = nb / 10;
	}
	return (o);
}
